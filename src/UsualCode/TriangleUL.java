package UsualCode;

public class TriangleUL {
    private int base;
    private int height;

    public TriangleUL(int base,int height){
        this.base=base;
        this.height=height;
    }

    public int getHeight() {
        return height;
    }

    public int getBase(){
        return base;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public void setBase(int base){
        this.base =base;
    }
}

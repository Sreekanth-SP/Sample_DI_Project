package UsualCode;

public class RectangleUL {
    private int length;
    private int breadth;

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getBreadth() {
        return breadth;
    }

    public void setBreadth(int breadth) {
        this.breadth = breadth;
    }

    public RectangleUL(int length, int breadth) {
        this.length = length;
        this.breadth = breadth;
    }
}
